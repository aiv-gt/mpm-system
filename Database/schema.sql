CREATE TABLE USERS(
  Uid INT PRIMARY KEY AUTO_INCREMENT,
  Username VARCHAR(30) UNIQUE,
  Password VARCHAR(255),
  Role VARCHAR(20),
  Active TINYINT(1) DEFAULT 1
);

