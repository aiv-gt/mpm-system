CREATE TABLE RAW_MATERIALS(
  Rid INT PRIMARY KEY AUTO_INCREMENT,
  Name VARCHAR(40),
  Stock_level INT,
  Unit VARCHAR(20),
  reorder_point INT DEFAULT 20,
  Sid INT,
  FOREIGN KEY (Sid) REFERENCES SUPPLIERS(Sid)
);

